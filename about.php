                <div class="mx-5">
                    <ul>
                        <li>
                            Every library user is required to present their validated personal ID every time he/she borrows books and other LRC materials at the loan/circulation counter. <br>
                            User should affix their signature at the book card bearing the same title and Library ID. <br>
                            Library user should not leave the counter unless all pages of books are inspected. A penalty will be imposed for torn pages upon return.
                        </li>
                        <li>Library ID and the book card are properly received by the circulation in charge before leaving the area upon returning of books and other library materials.</li>
                        <li>Students can borrow one reserve book for one-hour use within the LRC. Extension for additional hours is granted unless there is no prior request by other users for the same title of books. They may be borrowed overnight or for home use.</li>
                        <li>Non-reserve book or reference book/Filipiniana can be borrowed at three titles for home use for three days and renewable for another three days and for many times as long as there is no user’s requesting for the same title.</li>
                        <li>Fiction Books (novels) can be borrowed one title at a time, anytime of the day for seven days and renewable for another seven days, and for many times as long as there are no users requesting for the same title of books.</li>
                        <li>Borrowing a single copy book is allowed for classroom or reportorial use on condition that the borrower would secure written permission from his/her respective instructor.</li>
                        <li>Overnight books are allowed to be taken out from 3:00-5:00 P.M. Monday to Saturday. The reserve books are to be returned the following day on or before 9:00 o’clock A.M. and on Saturdays are to be returned on Monday at 8:00 o’clock A.M.</li>
                        <li>A user is allowed a maximum of three (3) times overdue beyond which the borrowing privileges are suspended for a period of one month.</li>
